-- using 123165033 as a seed to the RNG
select to_char(systimestamp, 'YYYY-MM-DD HH-MI-SS.FF3')  from dual;
set timing on;



CREATE VIEW REVENUE0 (SUPPLIER_NO, TOTAL_REVENUE) AS
	SELECT
		L_SUPPKEY,
		SUM(L_EXTENDEDPRICE * (1 - L_DISCOUNT))
	FROM
		LINEITEM
	WHERE
		L_SHIPDATE >= DATE'1996-10-01'
		AND L_SHIPDATE < ADD_MONTHS(DATE'1996-10-01', 3)
	GROUP BY
		L_SUPPKEY;


SELECT
	S_SUPPKEY,
	S_NAME,
	S_ADDRESS,
	S_PHONE,
	TOTAL_REVENUE
FROM
	SUPPLIER,
	REVENUE0
WHERE
	S_SUPPKEY = SUPPLIER_NO
	AND TOTAL_REVENUE = (
		SELECT
			MAX(TOTAL_REVENUE)
		FROM
			REVENUE0
	)
ORDER BY
	S_SUPPKEY;

DROP VIEW REVENUE0;

